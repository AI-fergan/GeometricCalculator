
public class Point {
	private int _x;
	private int _y;
	
	/* C'tors */
	Point(int x, int y) {
		this._x = x;
		this._y = y;
	}
	
	/* Getters */
	public int getX() {
		return this._x;
	}
	
	public int getY() {
		return this._y;
	}
	
	/* Setters */
	public void setX(int x) {
		this._x = x;
	}
	
	public void setY(int y) {
		this._y = y;
	}
}
